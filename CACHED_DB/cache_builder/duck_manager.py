import tempfile
import os

import duckdb
import mysql.connector
from datetime import date, timedelta, time as dt_time, datetime
import numpy as np


from config import MYSQL_CONFIG, DUCKDB_PATH, FETCH_BATCH_SIZE, LOAD_BATCH_DAYS
from logger import get_logger

logger = get_logger()


def _fix_row(row: tuple) -> tuple:
    """
    MySQL connector returns TIME columns as datetime.timedelta.
    DuckDB expects datetime.time — convert any timedelta value in the row.
    """
    result = []
    for val in row:
        if isinstance(val, timedelta):
            total_seconds = int(val.total_seconds())
            h = total_seconds // 3600
            m = (total_seconds % 3600) // 60
            s = total_seconds % 60
            result.append(dt_time(h, m, s))
        else:
            result.append(val)
    return tuple(result)


def _build_date_range(days_back: int = 1) -> tuple:
    today = date.today()
    return str(today - timedelta(days=days_back)), str(today)

def _date_batches(start_date: str, end_date: str, batch_days: int):
    """Yield (batch_start, batch_end) string pairs covering start_date..end_date."""
    current = datetime.strptime(start_date, "%Y-%m-%d").date()
    end     = datetime.strptime(end_date,   "%Y-%m-%d").date()
    while current <= end:
        batch_end = min(current + timedelta(days=batch_days - 1), end)
        yield str(current), str(batch_end)
        current = batch_end + timedelta(days=1)



